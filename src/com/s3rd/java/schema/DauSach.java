package com.s3rd.java.schema;

public class DauSach {
    private String maSach;
    private String tenSach;
    private int soTrang;
    private String tenTacGia;
    private int namXuatBan;
    private String theLoai;

    public void themDauSach(String maSach, String tenSach, int soTrang, String tenTacGia, int namXuatBan,
            String theLoai) {
        this.setMaSach(maSach);
        this.setTenSach(tenSach);
        this.setSoTrang(soTrang);
        this.setTenTacGia(tenTacGia);
        this.setNamXuatBan(namXuatBan);
        this.setTheLoai(theLoai);
    }

    public void xoaDauSach(DauSach dauSach) {
        dauSach = new DauSach();
    }

    public String getMaSach() {
        return maSach;
    }

    public void setMaSach(String maSach) {
        this.maSach = maSach;
    }

    public String getTenSach() {
        return tenSach;
    }

    public void setTenSach(String tenSach) {
        this.tenSach = tenSach;
    }

    public int getSoTrang() {
        return soTrang;
    }

    public void setSoTrang(int soTrang) {
        this.soTrang = soTrang;
    }

    public String getTenTacGia() {
        return tenTacGia;
    }

    public void setTenTacGia(String tenTacGia) {
        this.tenTacGia = tenTacGia;
    }

    public int getNamXuatBan() {
        return namXuatBan;
    }

    public void setNamXuatBan(int namXuatBan) {
        this.namXuatBan = namXuatBan;
    }

    public String getTheLoai() {
        return theLoai;
    }

    public void setTheLoai(String theLoai) {
        this.theLoai = theLoai;
    }

}
